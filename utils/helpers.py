@validate_call
def show_alert(
    type: Literal["genericError", "genericWarning", "genericSuccess"],
    body: str,
    status_code: int,
    title: str,
    message: str,
    triggers: dict = {},
):
    return (
        body,
        status_code,
        {
            "HX-Retarget": "body",
            "HX-Trigger": json.dumps(
                {
                    "showAlert": {
                        type: {
                            "title": title,
                            "message": message,
                        }
                    },
                    **triggers,
                }
            ),
        },
    )


@validate_call
def validation_error(
    errors: list = [], status_code: int = 422, message: str | None = None
):
    locations = []
    if errors:
        for loc in [l.get("loc") for l in errors if l.get("loc")]:
            i = 1
            _location = None
            if len(loc) > 1:
                while len(loc) > i:
                    if not _location:
                        _location = f"{loc[0]}[{loc[1]}]"
                        i = 2
                    else:
                        _location = f"{_location}[{loc[i]}]"
                        i += 1
                locations.append(_location)
            else:
                locations.append(loc)

        error_msgs = [
            l.get("msg").removeprefix("Value error, ") for l in errors if l.get("msg")
        ]
        if not error_msgs:
            error_msgs = ["Provided data could not be validated"]

    if message:
        error_msgs = [message]
    return (
        "",
        status_code,
        {
            "HX-Retarget": "body",
            "HX-Trigger": json.dumps(
                {
                    "showAlert": {
                        "validationError": {
                            "locations": locations,
                            "message": error_msgs,
                        }
                    }
                }
            ),
        },
    )


def user_session_required(fn):
    @wraps(fn)
    async def wrapper(*args, **kwargs):
        if (
            r.getex(f"user:session:{session.get('id', '')}", 30)
            != request.realm_data["id"]
        ):
            try:
                assert Path(f"realms/databases/{request.realm_data['id']}").exists()
            except AssertionError:
                logger.error("Realm ID provided in session data does not exist")
                session.clear()

            with TinyDB(
                **defaults.TINYDB,
                path=request.realm_data["path"],
            ) as db:
                if (
                    db.table("users").get(Query().id == session.get("id", ""))
                    and request.realm_data["id"] == session["realm_id"]
                ):
                    r.set(
                        f"user:session:{session.get('id', '')}",
                        session["realm_id"],
                        ex=30,
                    )
                    return await fn(*args, **kwargs)
        else:
            return await fn(*args, **kwargs)

        if "Hx-Request" in request.headers:
            return show_alert(
                "genericError",
                "",
                401,
                "Authentication required",
                "You need to be authenticated to access this resource",
            )
        else:
            return redirect("/", code=302)

    return wrapper


def realm_access_required(fn):
    @wraps(fn)
    async def wrapper(*args, **kwargs):
        if not kwargs.get("realm_id") or kwargs.get("realm_id") != session.get(
            "realm_id"
        ):
            if "Hx-Request" in request.headers:
                return show_alert(
                    "genericError",
                    "",
                    401,
                    "Realm access required",
                    "You do not have permission to access this realm",
                )
            else:
                return redirect("/", code=302)

        return await fn(*args, **kwargs)

    return wrapper


@validate_call
def string_to_tar_bytes(filename: str, data: str) -> bytes:
    tarinfo = tarfile.TarInfo(filename)
    tarinfo.size = len(data.encode("utf8"))
    out_tar = BytesIO()
    with tarfile.open(fileobj=out_tar, mode="w|") as tf:
        tf.addfile(tarinfo, BytesIO(data.encode("utf8")))
    out_tar.seek(0)
    return out_tar
