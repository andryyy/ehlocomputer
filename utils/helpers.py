def utc_now_as_str():
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S%z")


@validate_call
def get_realm_by_hostname(hostname: str):
    try:
        hostname = get_validated_fqdn(hostname)
        realm = r.getex(f"realmdata:{hostname}", ex=300)
        if realm:
            return json.loads(realm)
    except ValueError:
        logger.warning("Could not read a valid hostname from request, using 'default'")
        # "default" never from cache
        hostname = "default"

    with TinyDB("realms/realms.json") as db:
        realm = db.table("realms").get(Query().identity == hostname)
        if not realm:
            realm = db.table("realms").get(Query().default == True)
            if not realm:
                raise Exception("No realm available")

        assert Path(f"realms/databases/{realm['id']}").exists()
        realm.update(
            {
                "path": f"realms/databases/{realm['id']}",
            }
        )
        r.set(f"realmdata:{hostname}", json.dumps(realm), ex=300)
        return realm


@validate_call
def batch(l: list, n: int):
    _l = len(l)
    for ndx in range(0, _l, n):
        yield l[ndx : min(ndx + n, _l)]


@validate_call
def get_validated_fqdn(hostname: str) -> str:
    regex = re.compile(
        r"^((?![-])[-A-Z\d]{1,63}(?<!-)[.])*(?!-)[-A-Z\d]{1,63}(?<!-)?$", re.IGNORECASE
    )
    if len(hostname) > 253:
        raise ValueError(f"{hostname} is too long")
    if regex.match(hostname):
        return hostname
    else:
        raise ValueError(f"{hostname} is not a valid FQDN")


@validate_call
def ensure_list(s: str | list[str] | None) -> list:
    if s:
        if isinstance(s, str):
            return [s]
        if isinstance(s, list):
            return s
    return []


@validate_call
def str_lists_match(x: list[str], y: list[str]) -> bool:
    return sorted(x, key=lambda x: str(x)) == sorted(x, key=lambda x: str(y))


@validate_call
def str_lists_match_unique(x: list[str], y: list[str]) -> bool:
    return set(sorted(x, key=lambda x: str(x))) == set(sorted(y, key=lambda x: str(x)))


@validate_call
def to_unique_sorted_str_list(l: list[str]) -> list:
    _l = [x for x in set(l) if x != ""]
    return sorted(_l, key=lambda x: str(x))


@validate_call
def flatten(l: list[list]):
    return [i for sub_list in l for i in sub_list]


